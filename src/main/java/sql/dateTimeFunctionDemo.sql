-- 当前日期时间，日期，时间
SELECT NOW();-- 2025-12-29 16:50:27
SELECT CURDATE();-- 2025-12-29
SELECT CURTIME();-- 16:50:27

-- 日期加减 SECOND,MINUTE,HOUR,DAY,WEEK,MONTH,YEAR
SELECT DATE_ADD('2025-01-01',INTERVAL 7 DAY);-- 2025-01-08
SELECT DATE_ADD('2025-01-01',INTERVAL -1 MONTH);-- 2024-12-01
SELECT DATE_ADD('2025-01-01',INTERVAL -1 SECOND);-- 2024-12-31 23:59:59
SELECT DATE_ADD('2024-12-31 23:59:59',INTERVAL 1 SECOND);-- 2025-01-01 00:00:00
SELECT DATE_ADD(NOW(),INTERVAL 10 MINUTE);-- 2025-12-29 17:06:16

-- 时间差计算 TIMESTAMPDIFF
SELECT TIMESTAMPDIFF(DAY, '2025-01-01 10:00:00', '2025-01-03 09:00:00'); -- 1
SELECT TIMESTAMPDIFF(HOUR,'2025-01-01 10:00:00','2025-01-01 15:00:00'); -- 5

-- 日期格式化 DATE_FORMAT
SELECT DATE_FORMAT(NOW(),'%Y-%m-%d %H-%i-%s');-- 2025-12-29 16-54-32